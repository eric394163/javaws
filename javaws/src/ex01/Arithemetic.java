package ex01;

public class Arithemetic {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 2;
        int add = num1 + num2;
        int mul = num1 * num2;
        int sub = num1 - num2;
        float div = num1 / num2;
        System.out.println(num1 + "+" + num2 + "=" + add);
        System.out.println(num1 + "*" + num2 + "=" + mul);
        System.out.println(num1 + "-" + num2 + "=" + sub);
        System.out.println(num1 + "/" + num2 + "=" + div);
        
    }
}
