package ex01;

public class prefixincrement {
    public static void main(String[] args) {

        for (int num1 = 0; num1 < 10; num1++) {
            System.out.println(num1);
        }
        for (int num2 = 10; num2 > 0; num2--) {
            System.out.println(num2);
        }
    }
}
